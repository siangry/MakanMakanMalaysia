function createRecipeCard(recipe) {
    const card = document.createElement('div');
    card.className = 'recipe-card';
    card.innerHTML = `
        <img src="${recipe.image}" alt="${recipe.title}" class="recipe-image">
        <div class="recipe-info">
            <div class="recipe-category">${recipe.category}</div>
            <div class="recipe-title">${recipe.title}</div>
            <div class="recipe-author">By ${recipe.author}</div>
        </div>
        <div class="Ingredients">
            <h3>Ingredients:</h3>
            <ul>${recipe.ingredients.map(ing => `<li>${ing}</li>`).join('')}</ul>
        </div>
        <div class="Instructions">
            <h3>Instructions:</h3>
            <ol>${recipe.instructions.map(inst => `<li>${inst}</li>`).join('')}</ol>
        </div>
        <div class="youtube-video">
            <iframe width="310" height="300" src=${recipe.youtubeId} frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
        </div>
    `;
    return card;
}

function displayRecipes(category = 'all') {
    const container = document.getElementById('recipeContainer');
    container.innerHTML = ''; // Clear existing recipes
    recipes.forEach(recipe => {
        if (category === 'all' || recipe.category === category) {
            const card = createRecipeCard(recipe);
            container.appendChild(card);
        }
    });
}

function setupFilterButtons() {
    const buttons = document.querySelectorAll('.filter-button');
    buttons.forEach(button => {
        button.addEventListener('click', function() {
            buttons.forEach(btn => btn.classList.remove('active'));
            this.classList.add('active');
            const category = this.getAttribute('data-category');
            displayRecipes(category);
        });
    });
}

displayRecipes();
setupFilterButtons();


// Go To Top button
let mybutton = document.getElementById("myBtn");
            
window.onscroll = function() {scrollFunction()};

function scrollFunction() {
  if (document.body.scrollTop > 20 || document.documentElement.scrollTop > 20) {
    mybutton.style.display = "block";
  } else {
    mybutton.style.display = "none";
  }
}

function topFunction() {
  document.body.scrollTop = 0;
  document.documentElement.scrollTop = 0;
}
